type randomSortersHost = 
    private { 
        _projectDb: IGeneSortDb 
        _parameterSpans: (string * string list) list
        _filter: runParameters -> runParameters option
    }
    static member Create db spans filter =
        { _projectDb = db; _parameterSpans = spans; _filter = filter }

    member this.ProjectDb = this._projectDb
    member this.ParameterSpans = this._parameterSpans
    member this.Filter = this._filter

    /// Logic for calculating lengths based on model and width
    member this.GetStageLength (smt: sorterModelType) (sw: int<sortingWidth>) : int<stageLength> =
        match %sw with
        | 4 -> 5 |> UMX.tag
        | 6 -> 20 |> UMX.tag
        | 8 -> 40 |> UMX.tag
        | 12 -> 80 |> UMX.tag
        | 16 -> 100 |> UMX.tag
        | 18 -> 120 |> UMX.tag
        | 20 -> 130 |> UMX.tag
        | 22 -> 140 |> UMX.tag
        | 24 -> 200 |> UMX.tag
        | 32 -> 
            match smt with
            | sorterModelType.Msuf4 -> 600 |> UMX.tag
            | _ -> 300 |> UMX.tag
        | 64 -> 
            match smt with
            | sorterModelType.Msuf4 -> 2000 |> UMX.tag
            | sorterModelType.Msrs -> 800 |> UMX.tag
            | _ -> 600 |> UMX.tag
        | 128 -> 
            match smt with
            | sorterModelType.Msuf4 -> 4000 |> UMX.tag
            | sorterModelType.Msrs -> 1800 |> UMX.tag
            | sorterModelType.Mssi -> 1500 |> UMX.tag
            | _ -> 1200 |> UMX.tag
        | 256 -> 
            match smt with
            | sorterModelType.Msuf4 -> 6000 |> UMX.tag
            | sorterModelType.Msrs -> 4000 |> UMX.tag
            | _ -> 3000 |> UMX.tag
        | _ -> failwithf "Unsupported sorting width: %d" (%sw)

    /// Centralized safe parameter extraction
    member this.ExtractDomainParams (rp: runParameters) =
        maybe {
            let! smk = rp.GetSorterModelType()
            let! sw = rp.GetSortingWidth()
            let! sl = rp.GetStageLength()
            let! cl = rp.GetCeLength()
            let! sc = rp.GetSorterCount()
            return (smk, sw, sl, cl, sc)
        }

module RandomSorters =
    // --- Project Constants ---
    let projectName = "RandomSorters" |> UMX.tag<projectName>
    let projectFolder = "c:\\Projects\\RandomSorters\\Data" |> UMX.tag<pathToProjectFolder>
    let projectDesc = "RandomSorters, generated by Msce, Mssi, Msrs, and Msuf"
    let rngFactory = rngFactory.LcgFactory
    let excludeSelfCe = true

    // --- Shared Filtering Logic ---
    let private sorterModelTypeForSortingWidth (rp: runParameters) =
        let smt = rp.GetSorterModelType().Value
        let sw = rp.GetSortingWidth().Value
        let has2factor = (%sw % 2 = 0)
        let isMuf4able = (MathUtils.isAPowerOfTwo %sw)
        let isMuf6able = (%sw % 3 = 0) && (MathUtils.isAPowerOfTwo (%sw / 3))

        match smt with
        | sorterModelType.Msce -> Some rp
        | sorterModelType.Mssi | sorterModelType.Msrs -> if has2factor then Some rp else None
        | sorterModelType.Msuf4 -> if isMuf4able then Some rp else None
        | sorterModelType.Msuf6 -> if isMuf6able then Some rp else None
        | _ -> None

    let paramMapFilter (rp: runParameters) = 
        Some rp |> Option.bind sorterModelTypeForSortingWidth

    // --- Configuration P1 (Testing) ---
    module P1 =
        let spans = [ 
            (runParameters.sortingWidthKey, ["16"])
            (runParameters.sorterModelTypeKey, [sorterModelType.Msce] |> List.map SorterModelType.toString)
        ]
        let host = randomSortersHost.Create (new GeneSortDbMp(projectFolder) :> IGeneSortDb) spans paramMapFilter

    // --- Configuration P2 (Production) ---
    module P2 =
        let spans = [ 
            (runParameters.sortingWidthKey, ["16"; "32"; "64"; "128"; "256"])
            (runParameters.sorterModelTypeKey, 
                [sorterModelType.Msce; sorterModelType.Mssi; sorterModelType.Msrs; sorterModelType.Msuf4] 
                |> List.map SorterModelType.toString)
        ]
        let host = randomSortersHost.Create (new GeneSortDbMp(projectFolder) :> IGeneSortDb) spans paramMapFilter

    // --- Query Parameter Helpers ---
    let makeQueryParams (repl: int<replNumber> option) (sortingWidth:int<sortingWidth> option)
                        (sorterModelType:sorterModelType option) (outputDataType: outputDataType) =
        queryParams.create (Some projectName) repl outputDataType
            [| (runParameters.sortingWidthKey, sortingWidth |> SortingWidth.toString); 
               (runParameters.sorterModelTypeKey, sorterModelType |> Option.map SorterModelType.toString |> UmxExt.stringToString) |]

    let makeQueryParamsFromRunParams (runParams: runParameters) (outputDataType: outputDataType) =
        makeQueryParams (runParams.GetRepl()) (runParams.GetSortingWidth())
                        (runParams.GetSorterModelType()) outputDataType

    // --- Refinement ---
    let enhancer (host: randomSortersHost) (rp : runParameters) : runParameters =
        let sw = rp.GetSortingWidth().Value
        let smt = rp.GetSorterModelType().Value
        let qp = makeQueryParamsFromRunParams rp (outputDataType.RunParameters)
        let stageLength = host.GetStageLength smt sw
        let ceLength = stageLength |> StageLength.toCeLength sw
        let sorterCount = 100 |> UMX.tag<sorterCount>

        rp.WithProjectName(Some projectName)
          .WithRunFinished(Some false)
          .WithCeLength(Some ceLength)
          .WithStageLength(Some stageLength)
          .WithSorterCount(Some sorterCount)